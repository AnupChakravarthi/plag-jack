package anups.htclan.plag.pojo;

public class FileReadWordPojo {
	
  private String word; // Word in the Document
  private int wordCount; // Total Number of Times in the Document
  
  public String getWord() {
	return word;
  }
  
  public void setWord(String word) {
	this.word = word;
  }
  
  public int getWordCount() {
	return wordCount;
  }
  
  public void setWordCount(int repeatCount) {
	this.wordCount = repeatCount;
  }
  
}
