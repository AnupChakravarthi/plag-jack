package anups.htclan.plag.pojo;

public class FileReadLetterPojo {
 private String word;
 private String fileName;
 private int from;
 private int to;
 
 public String getWord() {
   return word;
 }
 
 public void setWord(String word) {
  this.word = word;
 }
 
 public String getFileName() {
	return fileName;
 }
 
 public void setFileName(String fileName) {
	this.fileName = fileName;
 }
 
 public int getFrom() {
	return from;
 }
 
 public void setFrom(int from) {
	this.from = from;
 }
 
 public int getTo() {
	return to;
 }
 
 public void setTo(int to) {
	this.to = to;
 }

}
